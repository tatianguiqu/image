package image.image;
public class ImageKey {
	String path;
	String c;
	double[] imageKey;
	public String getPath() {
		return path;
	}
	public void setPath(String path) {
		this.path = path;
	}
	public String getC() {
		return c;
	}
	public void setC(String c) {
		this.c = c;
	}
	public double[] getImageKey() {
		return imageKey;
	}
	public void setImageKey(double[] imageKey) {
		this.imageKey = imageKey;
	}
}
